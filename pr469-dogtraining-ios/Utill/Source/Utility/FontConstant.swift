import SwiftUI

//Manrope Bold
func ManropeBoldCustom(size :CGFloat) -> Font{
    let font:Font = Font.custom("Manrope-Bold", size: size)
    return font
}

//Manrope Medium
func ManropeMediumCustom(size :CGFloat) -> Font{
    let font:Font = Font.custom("Manrope-Medium", size: size)
    return font
}
